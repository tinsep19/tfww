## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# tfww completion                                          -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/dannyben/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_tfww_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    echo "$words"'
  echo $''
  echo $'  else'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" != "-" ]] && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $''
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_tfww_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=("${COMP_WORDS[@]:1:$COMP_CWORD-1}")'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  case "$compline" in'
  echo $'    \'completions\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'switch\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "$(ls envs/*.tfvars plans/*.tfplan 2>/dev/null) --help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'apply\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "$(ls envs/*.tfvars plans/*.tfplan 2>/dev/null) --all --destroy --help --plan-all -D -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'plan\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "$(ls envs/*.tfvars 2>/dev/null) --all --destroy --help --refresh-only -D -R -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'show\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "$(ls envs/*.tfvars plans/*.tfplan 2>/dev/null) --help --json -h -j")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'push\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "$(ls migrates/*.tfstate 2>/dev/null) --all --force --help --ignore-remote-version -f -h -r")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_tfww_completions_filter "--help --version -h -v apply completions plan push show switch")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'  complete -F _tfww_completions tfww'
  echo $''
  echo $'# ex: filetype=sh'
}